    private void Gioco() {
        int answer, mossaValida;
        mossaValida = g.play(x, y, 0);
        if (mossaValida == 1)
            JOptionPane.showMessageDialog(this, "M O S S A  N O N  V A L I D A!!!  R I P R O V A");
        else {
            mossaValida = g.play(x, y, 1);
            if (g.humanPoints() > 0 || g.pcPoints() > 0)
                segnalaPunti();
            Refresh();
            if (g.humanPoints() == 5) {
                answer = JOptionPane.showConfirmDialog(
                        this,
                        "VUOI GIOCARE ANCORA ?",
                        "H A I  V I N T O  T U  !",
                        JOptionPane.YES_NO_OPTION);
                if (answer == JOptionPane.OK_OPTION)
                    Restart();
                else
                    System.exit(0);
            }
            if (g.pcPoints() == 5) {
                answer = JOptionPane.showConfirmDialog(
                        this,
                        "VUOI GIOCARE ANCORA ?",
                        "H O  V I N T O  I O  !",
                        JOptionPane.YES_NO_OPTION);
                if (answer == JOptionPane.OK_OPTION)
                    Restart();
                else
                    System.exit(0);
            }
        }

    }

